// Exemplo simples de código C válido
// Demonstra declarações, operações e estruturas básicas

int main() {
    // Declaração de variáveis
    int a = 10;
    int b = 20;
    float resultado;
    
    // Operação matemática
    resultado = a + b;
    
    // Estrutura condicional
    if (a > 5) {
        resultado = resultado * 2.0;
    }
    
    // Loop while
    while (b > 0) {
        b = b - 1;
    }
    
    // Return
    return 0;
}

// Função simples
int somar(int x, int y) {
    int total = x + y;
    return total;
}

